/**
 * Represents the state of the keyboard inputs used in the game.
 */
class Keyboard {
    /** @type {boolean} - True if the 'A' key is pressed */
    A = false;

    /** @type {boolean} - True if the 'D' key is pressed */
    D = false;

    /** @type {boolean} - True if the 'E' key is pressed */
    E = false;

    /** @type {boolean} - True if the 'SPACE' key is pressed */
    SPACE = false;

    /** @type {boolean} - True if the 'ESC' key is pressed */
    ESC = false;
}
